function handle_export(): void
{
    $user = require_role(['admin', 'staff']);
    $type = preg_replace('/[^a-z]/', '', (string) ($_GET['type'] ?? 'appointment')) ?: 'appointment';
    $payload = report_payload($type);

    header('Content-Type: text/csv; charset=utf-8');
    header('Content-Disposition: attachment; filename="detabot-' . $type . '-report.csv"');

    $out = fopen('php://output', 'w');
    fputcsv($out, [$payload['title']]);
    fputcsv($out, ['Generated by', $user['username'], 'Generated at', date('Y-m-d H:i:s')]);
    fputcsv($out, []);
    fputcsv($out, ['Summary']);
    foreach ($payload['summary'] as $label => $value) {
        fputcsv($out, [$label, $value]);
    }
    fputcsv($out, []);

    if (!empty($payload['rows'])) {
        fputcsv($out, array_keys($payload['rows'][0]));
        foreach ($payload['rows'] as $row) {
            fputcsv($out, $row);
        }
    }

    log_activity('export_report', 'Exported ' . $type . ' report', (int) $user['userID']);
    exit;
}

function handle_appointment_slots(): void
{
    require_login();

    $date = normalize_appointment_date($_GET['date'] ?? null);
    $clinicID = (int) (clinic()['clinicID'] ?? 1);

    json_response([
        'ok' => true,
        'date' => $date,
        'slots' => appointment_slot_list($date, $clinicID),
    ]);
}

