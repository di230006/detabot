        $treatmentPerformed = trim((string) ($_POST['treatmentPerformed'] ?? ''));

        db_execute(
            'INSERT INTO tbl_health_book (userID, appointmentID, createdBy, title, `condition`, nextTreatment, nextDate, notes, chartData, treatmentPerformed) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)',
            [$patientID, $appointmentID, (int) $user['userID'], $title, $condition, $nextTreatment ?: null, $nextDate ?: null, $notes ?: null, $chartData ?: null, $treatmentPerformed ?: null]
        );
        $entryID = (int) db()->lastInsertId();

        $uploadFailures = [];
        $files = $_FILES['files'] ?? null;
        if (is_array($files) && isset($files['name']) && is_array($files['name'])) {
            $uploadDir = health_book_upload_dir();
            $allowedMimes = [
                'application/pdf' => 'pdf',
                'image/jpeg' => 'jpg',
                'image/pjpeg' => 'jpg',
                'image/png' => 'png',
                'image/webp' => 'webp',
            ];
            $maxBytes = 5 * 1024 * 1024;

            foreach ($files['name'] as $index => $name) {
                $error = (int) ($files['error'][$index] ?? UPLOAD_ERR_NO_FILE);
                if ($error === UPLOAD_ERR_NO_FILE) {
                    continue;
                }

                $tmpName = (string) ($files['tmp_name'][$index] ?? '');
                $size = (int) ($files['size'][$index] ?? 0);
                if ($error !== UPLOAD_ERR_OK || $tmpName === '' || $size <= 0 || $size > $maxBytes) {
                    $uploadFailures[] = (string) $name;
                    continue;
                }

                $mime = function_exists('mime_content_type') ? (string) mime_content_type($tmpName) : (string) ($files['type'][$index] ?? '');
                if (!isset($allowedMimes[$mime])) {
                    $uploadFailures[] = (string) $name;
                    continue;
                }

                $original = basename(str_replace('\\', '/', (string) $name));
                $stored = bin2hex(random_bytes(16)) . '.' . $allowedMimes[$mime];
                if (!move_uploaded_file($tmpName, $uploadDir . '/' . $stored)) {
                    $uploadFailures[] = $original;
                    continue;
                }

                $fType = trim((string) ($_POST['fileTypes'][$index] ?? 'other'));
                if (!in_array($fType, ['xray', 'cbct', 'intraoral', 'document', 'other'])) {
                    $fType = 'other';
                }

                db_execute(
                    'INSERT INTO tbl_health_book_file (entryID, originalName, storedName, mimeType, fileSize, fileType) VALUES (?, ?, ?, ?, ?, ?)',
                    [$entryID, $original, $stored, $mime, $size, $fType]
                );
            }
        }

        log_activity('save_health_entry', 'Health record entry for patient #' . $patientID);
        if ($uploadFailures) {
            flash('danger', 'Entry saved, but one or more files could not be attached. Use PDF, JPG, PNG, or WEBP under 5 MB.');
        } else {
            flash('success', 'Health record entry saved.');
        }
        redirect_to(page_url('healthbook', ['patient' => $patientID]));
    }

    if ($action === 'delete_health_entry') {
        require_role(['admin', 'staff']);

        $entryID = (int) ($_POST['entryID'] ?? 0);
        $entry = health_book_entry($entryID);
        if ($entry) {
            $uploadDir = health_book_upload_dir();
            foreach (health_book_files($entryID) as $file) {
                @unlink($uploadDir . '/' . $file['storedName']);
            }
            db_execute('DELETE FROM tbl_health_book_file WHERE entryID = ?', [$entryID]);
            db_execute('DELETE FROM tbl_health_book WHERE entryID = ?', [$entryID]);
            log_activity('delete_health_entry', 'Deleted health record entry #' . $entryID);
        }

        flash('success', 'Health record entry deleted.');
        redirect_to(page_url('healthbook', ['patient' => (int) ($entry['userID'] ?? 0)]));
    }

    if ($action === 'delete_health_file') {
        require_role(['admin', 'staff']);

        $fileID = (int) ($_POST['fileID'] ?? 0);
        $file = db_one('SELECT f.*, h.userID FROM tbl_health_book_file f JOIN tbl_health_book h ON h.entryID = f.entryID WHERE f.fileID = ?', [$fileID]);
        if ($file) {
            @unlink(health_book_upload_dir() . '/' . $file['storedName']);
            db_execute('DELETE FROM tbl_health_book_file WHERE fileID = ?', [$fileID]);
            log_activity('delete_health_file', 'Deleted health record file #' . $fileID);
        }

        flash('success', 'File removed.');
        redirect_to(page_url('healthbook', ['patient' => (int) ($file['userID'] ?? 0)]));
    }

    if ($action === 'save_dental_record') {
        require_role(['admin', 'staff']);

        $patientID     = (int)    ($_POST['userID']          ?? 0);
        $apptID        = (int)    ($_POST['appointmentID']   ?? 0) ?: null;
        $toothNumber   = trim((string) ($_POST['toothNumber']    ?? ''));
        $diagnosis     = trim((string) ($_POST['diagnosis']      ?? ''));
        $treatmentDone = trim((string) ($_POST['treatmentDone']  ?? ''));
        $toothCond     = (string) ($_POST['toothCondition']   ?? 'good');
        $nextAction    = trim((string) ($_POST['nextAction']     ?? ''));
        $dentistNotes  = trim((string) ($_POST['dentistNotes']   ?? ''));

        $validConditions = ['good', 'monitor', 'needs_treatment', 'extracted'];
        if (!in_array($toothCond, $validConditions, true)) {
            $toothCond = 'good';
        }

        if ($patientID <= 0 || $diagnosis === '' || $treatmentDone === '') {
            flash('danger', 'Patient, diagnosis, and treatment done are required.');
            redirect_to(page_url('staff_health_record', ['patient' => $patientID]));
        }

        $patient = db_one(
            "SELECT userID FROM tbl_user WHERE userID = ? AND userRole = 'patient' AND status = 'active'",
            [$patientID]
        );
        if (!$patient) {
            flash('danger', 'Patient not found.');
            redirect_to(page_url('staff_health_record'));
        }

        db_execute(
            'INSERT INTO tbl_dental_record
             (userID, appointmentID, toothNumber, diagnosis, treatmentDone, toothCondition, nextAction, dentistNotes, recordedBy)
             VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)',
            [
                $patientID,
                $apptID,
                $toothNumber !== '' ? $toothNumber : null,
                $diagnosis,
                $treatmentDone,
                $toothCond,
                $nextAction !== '' ? $nextAction : null,
                $dentistNotes !== '' ? $dentistNotes : null,
                (int) $user['userID'],
            ]
        );

        log_activity('save_dental_record', 'Added dental record for patient #' . $patientID, (int) $user['userID']);
        flash('success', 'Dental record saved successfully.');
        redirect_to(page_url('staff_health_record', ['patient' => $patientID]));
    }

    if ($action === 'update_appointment') {
        require_role(['admin', 'staff']);

        $apptID = (int) ($_POST['appointmentID'] ?? 0);
        $date   = (string) ($_POST['appointmentDate'] ?? '');
        $time   = substr((string) ($_POST['appointmentTime'] ?? ''), 0, 5);
        $status = (string) ($_POST['status'] ?? '');

        $validStatuses = ['pending', 'confirmed', 'completed', 'cancelled'];
        if ($apptID <= 0 || !preg_match('/^\d{4}-\d{2}-\d{2}$/', $date) || !in_array($time, build_time_slots(), true) || !in_array($status, $validStatuses, true)) {
            flash('danger', 'Invalid appointment data.');
            redirect_to(page_url('appointments'));
        }

        $appt = db_one('SELECT * FROM tbl_appointment WHERE appointmentID = ?', [$apptID]);
        if (!$appt) {
            flash('danger', 'Appointment not found.');
            redirect_to(page_url('appointments'));
        }

        db_execute(
            "UPDATE tbl_appointment SET appointmentDate = ?, appointmentTime = ?, status = ?, updatedDate = CURRENT_TIMESTAMP WHERE appointmentID = ?",
            [$date, $time . ':00', $status, $apptID]
        );

        if ($status === 'completed' && (string) $appt['status'] !== 'completed') {
            award_completion_points($appt);
        }

        log_activity('update_appointment', 'Updated appointment #' . $apptID . ' status to ' . $status);
        flash('success', 'Appointment updated.');
        redirect_to(page_url('appointments'));
    }

    if ($action === 'cancel_appointment') {
        $apptID  = (int) ($_POST['appointmentID'] ?? 0);
        if ($apptID <= 0) {
            flash('danger', 'Invalid appointment.');
            redirect_to(page_url('appointments'));
        }

        $isStaff = has_role($user, ['admin', 'staff']);
        $appt = $isStaff
            ? db_one('SELECT * FROM tbl_appointment WHERE appointmentID = ?', [$apptID])
            : db_one('SELECT * FROM tbl_appointment WHERE appointmentID = ? AND userID = ?', [$apptID, (int) $user['userID']]);

        if (!$appt) {
            flash('danger', 'Appointment not found.');
            redirect_to(page_url('appointments'));
        }

        if (!in_array((string) $appt['status'], ['pending', 'confirmed'], true)) {
            flash('danger', 'Only pending or confirmed appointments can be cancelled.');
            redirect_to(page_url('appointments'));
        }

        db_execute(
            "UPDATE tbl_appointment SET status = 'cancelled', updatedDate = CURRENT_TIMESTAMP WHERE appointmentID = ?",
            [$apptID]
        );

        log_activity('cancel_appointment', 'Cancelled appointment #' . $apptID, (int) $user['userID']);
        flash('success', 'Appointment cancelled.');
        redirect_to(page_url('appointments'));
    }

    // No recognised action — do nothing, just redirect cleanly
    redirect_to(page_url('dashboard'));
}

function json_response(array $payload, int $status = 200): void
{
    http_response_code($status);
    header('Content-Type: application/json');
    echo json_encode($payload);
    exit;
}
